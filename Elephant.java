package com.xworkz.inheritance;


public class Elephant extends Animal {
    private int weight;
    private int age;

    public void setWeight(int weight) {
        this.weight = weight;
    }
    public int getWeight() {
        return weight;
    }

    public void setAge(int age) {
        this.age = age;
    }
    public int getAge() {
        return age;
    }
}

